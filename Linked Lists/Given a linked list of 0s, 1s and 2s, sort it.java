class Solution
{
    static Node segregate(Node head)
    {
        //Add code here
        Node crt=head;
        int zero=0,one=0,two=0;
        while(crt!=null)
        {
            if(crt.data==0) zero++;
            else if(crt.data==1) one++;
            else two++;
            
            crt=crt.next;
        }
        
        crt=head;
        while(crt!=null)
        {
            if(zero>0)
            {
                crt.data=0;
                zero--;
            }
            else if(one>0)
            {
                crt.data=1;
                one--;
            }
            else
            {
                crt.data=2;
                two--;
            }
            crt=crt.next;
        }
        return head;
    }
}